package com.company;

public class OPoint {
    private double x,y;


    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    OPoint(int x, int y){

        this.x = x;
        this.y = y;
    }


}
